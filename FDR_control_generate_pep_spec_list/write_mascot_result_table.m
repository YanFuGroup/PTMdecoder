function write_mascot_result_table(result, filename)

fout = fopen(filename,'w');
if fout == -1
    error(['Filed to open the file "',filename,'"!']);
end
fprintf(fout, ['Site\tDatasetName\tScan\tSpectrum\tCharge\t' ...
    'Calc_neutral_pepmass\tprecursor_neutral_mass\tmassdiff' ...
    '\tnum_match_ions\tpeptide\tprotein\tmodification\t' ...
    'modificationlocation\tScore\n']);
for i_fr = 1:length(result)
    fprintf(fout, '%s\t%s\t%s\t%s\t%s\t%f\t%f\t%f\t%d\t%s\t%s\t%s\t%s\t%f\n',...
        result(i_fr).Site,result(i_fr).DatasetName,...
        result(i_fr).Scan,result(i_fr).Spectrum,...
        result(i_fr).Charge,result(i_fr).Calc_neutral_pepmass,...
        result(i_fr).precursor_neutral_mass,result(i_fr).massdiff,...
        result(i_fr).num_match_ions,result(i_fr).peptide,...
        result(i_fr).protein,result(i_fr).modification,...
        result(i_fr).modificationlocation,result(i_fr).Score);
end
fclose(fout);
end